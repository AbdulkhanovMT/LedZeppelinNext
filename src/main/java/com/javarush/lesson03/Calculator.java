package com.javarush.lesson03;


class Calculator {


     double add(int a, int b) {
        return a + b;
    }

     double subtract(int a, int b) {
        return a - b;
    }

    double multiply(int a, int b) {
        return a * b;
    }

    double divide(int a, int b) {
        return (double) a / b;
    }
}
